    public void subjectLoad(){
       int c;
        try {
            pst = conn.prepareStatement("select * from subject");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(rs.next()){
              Vector v = new Vector();
              
              for(int i =1 ; i <=c ; i++){               
                v.add(rs.getString("sid"));
                v.add(rs.getString("subjectname"));               
              }
                 d.addRow(v);            
            }          
        } catch (SQLException ex) {
            Logger.getLogger(classes.class.getName()).log(Level.SEVERE, null, ex);
        }            
    }


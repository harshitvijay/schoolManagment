    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        try {
            String username = txtname.getText();
            String pass   =   txtpass.getText();
            String usertype = txtutype.getSelectedItem().toString();
            
            pst = conn.prepareStatement("select * from user where username = ? and password = ? and usertype = ?");
            pst.setString(1,username);
            pst.setString(2,pass);
            pst.setString(3,usertype);
            rs = pst.executeQuery();
            
            if(rs.next()){
               
                  int id = rs.getInt("id");
                  String phoneNo = rs.getString("phone");
                  this.setVisible(false);
                  new Main(id,username,usertype,phoneNo).setVisible(true);

            }
            else{
            
                    JOptionPane.showMessageDialog(null,"Username and password does not match.");
                    txtname.setText("");
                    txtpass.setText("");
                    txtutype.setSelectedIndex(-1);
                    txtname.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_loginActionPerformed

   

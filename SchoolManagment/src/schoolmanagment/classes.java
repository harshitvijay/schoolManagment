    public void loadClass(){
        int c;
        try {
            pst = conn.prepareStatement("select * from class");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            c = rsd.getColumnCount();
            
            d = (DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(rs.next()){
              Vector v = new Vector();
              for(int i =1 ; i <=c ; i++){  
               
                v.add(rs.getString("cid"));
                v.add(rs.getString("classname"));
                v.add(rs.getString("section"));
              }
                 d.addRow(v);            
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(classes.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }

    
